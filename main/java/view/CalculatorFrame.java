    private double calculateByAmountPromotion(double price, double tax, double fee, double promotion) {
        double finalPrice = 0;
        tax = tax/100;
        finalPrice = ((price + tax*price + fee) - promotion);
        return finalPrice;
    }
    
    private double calculateByPercentagePromotion(double price, double tax, double fee, double promotion) {
        double finalPrice = 0;
        tax = tax/100;
        promotion = promotion/100 * price;
        finalPrice = ((price + tax*price + fee) - promotion);
        return finalPrice;
    }
