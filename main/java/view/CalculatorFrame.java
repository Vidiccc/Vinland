    private void caculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_caculateButtonActionPerformed
        String sPrice = priceFormattedTextField.getText();
        double price = Double.parseDouble(sPrice);
        
        String sTax = taxFormattedTextField.getText();
        double tax = Double.parseDouble(sTax);
        
        String sFee = feeFormattedTextField.getText();
        double fee = Double.parseDouble(sFee);
        
        String sPromotion = promotionFormattedTextField.getText();
        double promotion = Double.parseDouble(sPromotion);
        
        String sDeposit = depositFormattedTextField.getText();
        double deposit = Double.parseDouble(sDeposit);
        
        deposit = deposit/100 * price;
        finalDepositLabel.setText("Deposit: " + deposit);
        
        if(byAmountRadioButton.isSelected()) {
            double finalPrice = calculateByAmountPromotion(price, tax, fee, promotion);
            finalPriceLabel.setText("Final price: " + finalPrice);
        }else{
            double finalPrice = calculateByPercentagePromotion(price, tax, fee, promotion);
            finalPriceLabel.setText("Final price: " + finalPrice);
        }
        
        
        
    }//GEN-LAST:event_caculateButtonActionPerformed

